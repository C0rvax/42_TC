#include "libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	void	*result;

	if ((int)nmemb < 0 || (int)size < 0)
		return (NULL);
	result = malloc(nmemb * size);
	if (!result)
		return (NULL);
	ft_bzero(result, nmemb * size);
	return (result);
}
